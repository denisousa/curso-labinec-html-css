var x = prompt("Digite um número X: ")
var y = prompt("Digite um número Y: ")

x = parseInt(x)
y = parseInt(y)

var soma = x + y
var sub = x - y
var mult = x * y
var div = x / y
console.log(`O resultado da soma entre ${x} + ${y} = ${soma}`)
console.log(`O resultado da soma entre ${x} - ${y} = ${sub}`)
console.log(`O resultado da soma entre ${x} * ${y} = ${mult}`)
console.log(`O resultado da soma entre ${x} / ${y} = ${div}`)
